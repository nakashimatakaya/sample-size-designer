# 結果パネル用「読み解きヒント」（デザイン別）
# calc_mode で sample_size / power_calc を切り替える。
# =========================================================================
result_hint_text <- function(design_id, params, result, power_target = 0.80,
                             calc_mode = "sample_size") {
  if (calc_mode == "power_calc") {
    return(.hint_power_calc(design_id, params, result))
  }
  pw_pct <- sprintf("%d", round(power_target * 100))
  drop_pct <- sprintf("%d", round(params$dropout * 100))

  common_tail <- paste(
    "",
    sprintf("脱落率 %s%% を見込んで登録数は増やしてありますが、", drop_pct),
    "実際の脱落率が想定を超える場合は再計算が必要です。",
    sep = "\n"
  )
  graph_tail <- paste(
    "もし実際の差が想定より小さい場合、同じ症例数では検出力が下がります。",
    "「グラフ」タブで、差を小さめに見積もったときの検出力を",
    "確認することを推奨します。",
    sep = "\n"
  )

  body <- switch(design_id,
    ttest_m1 = sprintf(
      paste(
        "この数字は、想定した差（A 群平均 %.2f と B 群平均 %.2f の差）が",
        "本当に存在するとき、%s%% の確率でそれを統計的に検出できる",
        "最小の症例数です。",
        sep = "\n"
      ),
      params$mean_A, params$mean_B, pw_pct
    ),
    ttest_m2 = sprintf(
      paste(
        "この数字は、想定した群間差 Δ = %.2f が本当に存在するとき、",
        "%s%% の確率でそれを統計的に検出できる最小の症例数です。",
        sep = "\n"
      ),
      params$diff, pw_pct
    ),
    paired = sprintf(
      paste(
        "この数字は、想定した差の平均 %.2f が本当に存在するとき、",
        "対応のある t 検定で %s%% の確率で有意と判定できる最小のペア数です。",
        sep = "\n"
      ),
      params$diff_mean, pw_pct
    ),
    paired_corr = sprintf(
      paste(
        "この数字は、治療前 %.2f・治療後 %.2f（相関係数 r = %.2f）から",
        "換算された差の SD = %.2f、差の平均 = %.2f のとき、",
        "対応のある t 検定で %s%% の確率で有意と判定できる",
        "最小のペア数です。",
        "r を小さめに見積もると差のばらつきが大きくなり、",
        "必要ペア数は増えます（保守的になります）。",
        sep = "\n"
      ),
      params$mean_1, params$mean_2, params$r,
      result$sd_diff, result$diff_mean, pw_pct
    ),
    binary_chisq = sprintf(
      paste(
        "この数字は、A 群 %d%%、B 群 %d%% の発生割合が本当にあるとき、",
        "χ² 検定で %s%% の確率で有意と判定できる最小の各群症例数です。",
        sep = "\n"
      ),
      round(params$p_A * 100), round(params$p_B * 100), pw_pct
    ),
    binary_fisher = sprintf(
      paste(
        "この数字は、Fisher の正確検定を想定した際の近似的な必要症例数です",
        "（χ² 近似を採用しているため、Fisher は若干保守的＝もう少し多めの",
        "人数が必要になる可能性があります）。",
        "希少事象（p が 0.05 未満または 0.95 超）ではシミュレーションに基づく",
        "検出力計算の併用を推奨します。",
        sep = "\n"
      )
    ),
    one_mean = sprintf(
      paste(
        "この数字は、%s%% 信頼区間の半幅が %s 以下になるような",
        "平均値の推定に必要な症例数です。",
        "検出力（有無の判定確率）という概念はありません。",
        sep = "\n"
      ),
      sprintf("%d", round(params$conf_level * 100)),
      format(params$half_width)
    ),
    one_prop = sprintf(
      paste(
        "この数字は、%s%% 信頼区間（%s）の半幅が %s 以下になるような",
        "割合推定に必要な症例数です。",
        "検出力の概念はありません。",
        sep = "\n"
      ),
      sprintf("%d", round(params$conf_level * 100)),
      switch(params$method, normal = "正規近似", wilson = "Wilson", exact = "Exact"),
      format(params$half_width)
    ),
    ttest_ni = sprintf(
      paste(
        "この数字は、非劣性マージン M = %.2f のもとで、",
        "片側 α = %s の検定で %s%% の確率で「非劣性」と判定できる",
        "最小の各群症例数です。",
        "M は平均差の単位で設定しています。",
        sep = "\n"
      ),
      params$margin, sprintf("%.3f", params$alpha), pw_pct
    ),
    paired_ni = sprintf(
      paste(
        "この数字は、マージン M = %.2f のもとで、対応のある片側 t 検定で",
        "%s%% の確率で「非劣性」と判定できる最小のペア数です。",
        sep = "\n"
      ),
      params$margin, pw_pct
    ),
    binary_ni = sprintf(
      paste(
        "この数字は、リスク差マージン M = %d%% のもとで、",
        "Chow ら (2018) の正規近似公式に基づいて %s%% の確率で",
        "「非劣性」と判定できる最小の各群症例数です。",
        "より正確な計算には Farrington-Manning 法の使用を推奨します。",
        sep = "\n"
      ),
      round(params$margin * 100), pw_pct
    ),
    "（デザイン別のヒントは未定義です）"
  )

  has_graph <- !(design_id %in% c("one_mean", "one_prop"))
  paste(body, if (has_graph) graph_tail else NULL, common_tail, sep = "\n")
}

# 検出力モードのヒント（各デザイン）
.hint_power_calc <- function(design_id, params, result) {
  pw_pct <- sprintf("%.1f", (result$achieved_power %||% 0) * 100)
  n <- params$n
  tail <- paste(
    "",
    "検出力が目標（通常 80% や 90%）に足りないときは、",
    "サイドバー上部の「計算モード」を「必要症例数を計算する」に切替えて",
    "必要 n を見積もってください。",
    sep = "\n"
  )
  body <- switch(design_id,
    ttest_m1 = sprintf(paste(
        "各群 %d 例の条件で、想定した差（A 群平均 %.2f・B 群平均 %.2f）を",
        "統計的に有意と判定できる確率は %s%% と計算されました。",
        sep = "\n"),
      n, params$mean_A, params$mean_B, pw_pct),
    ttest_m2 = sprintf(paste(
        "各群 %d 例の条件で、想定した群間差 Δ = %.2f を",
        "統計的に有意と判定できる確率は %s%% と計算されました。",
        sep = "\n"),
      n, params$diff, pw_pct),
    paired = sprintf(paste(
        "%d ペアの条件で、想定した差の平均 %.2f を",
        "対応のある t 検定で有意と判定できる確率は %s%% と計算されました。",
        sep = "\n"),
      n, params$diff_mean, pw_pct),
    paired_corr = sprintf(paste(
        "%d ペアの条件で、治療前 %.2f・治療後 %.2f・相関係数 %.2f",
        "（換算された差の SD = %.2f）から想定される差を",
        "対応のある t 検定で有意と判定できる確率は %s%% と計算されました。",
        sep = "\n"),
      n, params$mean_1, params$mean_2, params$r,
      result$sd_diff %||% NA_real_, pw_pct),
    binary_chisq = sprintf(paste(
        "各群 %d 例の条件で、A 群 %d%%・B 群 %d%% の発生割合の差を",
        "χ² 検定で有意と判定できる確率は %s%% と計算されました。",
        sep = "\n"),
      n, round(params$p_A * 100), round(params$p_B * 100), pw_pct),
    binary_fisher = sprintf(paste(
        "各群 %d 例の条件で、A 群 %d%%・B 群 %d%% の発生割合の差を",
        "Fisher の正確検定（χ² 近似）で有意と判定できる確率は",
        "%s%% と計算されました。",
        sep = "\n"),
      n, round(params$p_A * 100), round(params$p_B * 100), pw_pct),
